def main():
    with open(JSON_PATH, encoding="utf-8") as f:
        data = json.load(f)

    features_section = render_features(data["key_common_features"])
    rest_sections = "\n".join([
        prose_section("Actionable search criteria", "02", data["actionable_search_criteria"], "search-criteria"),
        prose_section("Success factors", "03", data["success_factors"], "success-factors"),
        prose_section("Potential gaps", "04", data["potential_gaps"], "potential-gaps"),
        prose_section("Recommendations", "05", data["recommendations"], "recommendations"),
    ])

    page = PAGE.format(
        css=CSS,
        logo_uri=logo_data_uri(),
        methodology=esc(data["methodology_note"]),
        n_features=len(data["key_common_features"]),
        features_section=features_section,
        rest_sections=rest_sections,
    )

    with open(OUT_PATH, "w", encoding="utf-8") as f:
        f.write(page)
    print(f"Wrote {OUT_PATH} ({len(page)} bytes)")


if __name__ == "__main__":
    main()
