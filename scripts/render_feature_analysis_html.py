"""Renders analysis/feature_analysis.json into analysis/feature_analysis_report.html.
Purely mechanical (like parse_md_to_csv.py / build_site.py) — free to re-run, no API calls.
The JSON itself is the authored analysis output and is not regenerated by this script;
re-run scripts/compute_feature_stats.py first if you want to refresh the underlying numbers,
then update feature_analysis.json by hand before re-rendering."""

import html as htmllib
import json
import os
import re

import base64

REPO_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
JSON_PATH = os.path.join(REPO_ROOT, "analysis", "feature_analysis.json")
OUT_PATH = os.path.join(REPO_ROOT, "analysis", "feature_analysis_report.html")
LOGO_PATH = os.path.join(REPO_ROOT, "docs", "assets", "reuse-logo.png")


def logo_data_uri():
    with open(LOGO_PATH, "rb") as f:
        b64 = base64.b64encode(f.read()).decode("ascii")
    return f"data:image/png;base64,{b64}"


def esc(s):
    return htmllib.escape(str(s), quote=False)


def prevalence_class(p):
    p = p.lower()
    if "high" in p and "rising" not in p:
        return "prev-high"
    if "rising" in p or "medium" in p:
        return "prev-medium"
    if "low" in p:
        return "prev-low"
    return "prev-medium"


def render_features(features):
    blocks = []
    for feat in features:
        ex_html = ""
        if feat["examples"]:
            ex_items = ", ".join(esc(e) for e in feat["examples"])
            ex_html = f'<p class="examples"><span class="examples-label">Examples</span> {ex_items}</p>'
        blocks.append(f"""
    <article class="feature">
      <div class="feature-head">
        <h3>{esc(feat['feature'])}</h3>
        <span class="pill {prevalence_class(feat['prevalence'])}">{esc(feat['prevalence'])}</span>
      </div>
      <p class="feature-desc">{esc(feat['description'])}</p>
      {ex_html}
    </article>
    """)
    return "\n".join(blocks)


def prose_section(title, number, text, id_):
    parts = re.split(r"(?=\d\)\s)", text)
    parts = [p.strip() for p in parts if p.strip()]
    if len(parts) > 1:
        cleaned = [re.sub(r"^\d\)\s*", "", p) for p in parts]
        items = "".join(f"<li>{esc(c)}</li>" for c in cleaned)
        body = f'<ol class="reco-list">{items}</ol>'
    else:
        body = f"<p>{esc(text)}</p>"
    return f"""
    <section class="report-section" id="{id_}">
      <div class="section-head">
        <span class="section-num">{number}</span>
        <h2>{title}</h2>
      </div>
      {body}
    </section>
    """


